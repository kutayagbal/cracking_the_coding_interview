package ch07_object_oriented_design.q3_jukebox;

public class Song {
	private String name;
	private String singer;
	private String album;
	private Genre genre;
	
	public String getName() {
		return name;
	}
	public String getSinger() {
		return singer;
	}
	public String getAlbum() {
		return album;
	}
	public Genre getGenre() {
		return genre;
	}
	
	
}
